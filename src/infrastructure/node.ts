const TIME_OUT = 60 * 1000;

/**
 * 入力された配列を非破壊にランダムに並び替えて返す
 */
export const shuffle: <T>(v: T[]) => T[] = ([...array]) => {
  for (let i = array.length - 1; i >= 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
};


export default class Node extends Base {
  /**
   * ノードの情報を取得する
   */
  static async getNodeInfo(uri: string): Promise<NodeInfo> {
    // get node info
    const nodeInfoUrl = this.getRestUri(uri, "node", "info");
    const nodeInfoRequestResult = await this.request(nodeInfoUrl, undefined, { timeout: TIME_OUT });
    const nodeInfoResponse: ResponseNodeInfo = await nodeInfoRequestResult.json();
    return {
      type: nodeInfoResponse.networkIdentifier,
      publicKey: nodeInfoResponse.publicKey,
      url: uri,
    };
  }

  /**
   * 接続試験の上、接続用のノードをランダムに返す
   */
  static async getRandomNode(networkType: NetworkType): Promise<NodeInfo> {
    const nodeList = networkType === NetworkType.MAIN_NET ? shuffle(mainNetNodeList()) : shuffle(testNetNodeList());

    for (let i = 0; i < nodeList.length; i++) {
      try {
        const u = this.getRestUri(nodeList[i].apiStatus.restGatewayUrl, "node", "info");
        const response = await this.request(u, undefined, { timeout: TIME_OUT });
        const json = await response.json() as ResponseNodeInfo;
        return {
          type: networkType,
          publicKey: json.publicKey,
          url: nodeList[i].apiStatus.restGatewayUrl,
        };
      } catch (_) { }
    }

    throw new SystemError("接続先候補なし", { console: "全てのノードへ検証を試みましたが、全て不可でした", view: "通信エラー" });
  }
}

